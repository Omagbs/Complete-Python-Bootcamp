# Part two of illustrating Math Module
import math

x = 7.5
y = 81
z = 6

print("x value is", x)
print("y value is", y)
print("z value is", z)
print("The ceil of x Value is: ", math.ceil(x))  # rounds down to the nearest number
print("The floor of x value is: ", math.floor(x))  # rounds up to the nearest number
print("The square root of z value is: ", math.isqrt(y))  # takes the square root of a number
print("The cube of z is: ", pow(z, 3))  # takes the cube of a number
