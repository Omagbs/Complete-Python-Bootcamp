# Set containers in Python
# They immutable
# they make use of the curly brackets {}
# Duplicates are removed

# Creating a Set
MySet = {1, 2, 3}
print(MySet)

# Converting a list to a set also taking out duplicates
MySet2 = set([1, 2, 3, 2])
print(MySet2)

# Creating an empty set
MySet3 = set()
print(MySet3)

MySet4 = {5, 6, 7}
MySet5 = {8, 9, 10}

print(6 in MySet4)